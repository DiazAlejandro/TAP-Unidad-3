    private void tablaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tablaKeyReleased
        //Actualizar campos de la tabla al modificar una columna.
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            //Obtiene el valor de la cantidad a comprar
            int comp = Integer.parseInt(String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 4)));
            //Obtiene el valor de la cantidad disponible
            int cant = Integer.parseInt(String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 2)));
            //Obtiene el precio de cada producto
            double precio = Double.parseDouble(String.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 3)));

            //Operaciones
            if (comp < cant){
                int rest = cant - comp;
                double tot = precio * comp;
                //Asigna el valor a las columnas de las tablas
                tabla.setValueAt(rest, tabla.getSelectedRow(), 2);
                tabla.setValueAt(tot, tabla.getSelectedRow(),5);                
            }else{
                JOptionPane.showMessageDialog(null, "La cantidad a comprar tiene que ser menor a la disponibilidad");
                tabla.setValueAt(0,tabla.getSelectedRow(), 4);
            }
        }
    }//GEN-LAST:event_tablaKeyReleased

